class ProfileModel {
  final String nome;
  final String email;
  final int idade;
  final String cpf;
  final String cep;
  final String endereco;
  final String numero;
  final String celular;

  ProfileModel({
    required this.nome,
    required this.email,
    required this.idade,
    required this.cpf,
    required this.cep,
    required this.endereco,
    required this.numero,
    required this.celular,
  });
}
